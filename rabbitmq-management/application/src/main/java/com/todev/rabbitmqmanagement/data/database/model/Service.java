package com.todev.rabbitmqmanagement.data.database.model;

import com.raizlabs.android.dbflow.annotation.Column;
import com.raizlabs.android.dbflow.annotation.PrimaryKey;
import com.raizlabs.android.dbflow.annotation.Table;
import com.raizlabs.android.dbflow.structure.BaseModel;
import com.todev.rabbitmqmanagement.data.database.Database;
import lombok.Data;
import lombok.EqualsAndHashCode;
import lombok.NoArgsConstructor;

@Data
@EqualsAndHashCode(callSuper = true)
@NoArgsConstructor
@Table(name = Service.TABLE_NAME, database = Database.class)
public class Service extends BaseModel {
  public static final String TABLE_NAME = "Service2";

  @Column @PrimaryKey(autoincrement = true) int id;
  @Column String label;
  @Column String address;
  @Column int port;
}
